#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	ptr = (unsigned char *)str;
	i = 0;
	while (i < n)
	{
		if (*ptr == c)
			return (ptr);
		ptr++;
		i++;
	}
	return (NULL);
}
