#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	new->next = *lst;
	*lst = new;
}

/*
t_list **lst because pass in the address of pointer
to the head of the node, &head
t_list *new because ur passing in the pointer
to a new node, node1
*/
